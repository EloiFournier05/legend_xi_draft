import type { HiddenAttributes, HistoricalSquad, LegendTag, Player, Position } from "../types/game";

type SeedPlayer = [string, Position[], number, LegendTag?];

interface SquadSeed {
  id: string;
  clubName: string;
  season: string;
  country: string;
  strengthTier: 1 | 2 | 3 | 4 | 5;
  players: SeedPlayer[];
}

const clamp = (value: number, min = 35, max = 99) => Math.max(min, Math.min(max, Math.round(value)));

const positionProfile = (positions: Position[], overall: number): HiddenAttributes => {
  const primary = positions[0];
  const isGk = primary === "GK";
  const isDef = ["LB", "CB", "RB", "LWB", "RWB"].includes(primary);
  const isMid = ["CDM", "CM", "CAM", "LM", "RM"].includes(primary);
  const isForward = ["LW", "RW", "CF", "ST"].includes(primary);

  return {
    overall,
    attack: clamp(overall + (isForward ? 7 : isMid ? 1 : isDef ? -9 : -35)),
    defense: clamp(overall + (isDef ? 6 : primary === "CDM" ? 5 : isMid ? -2 : isGk ? -20 : -18)),
    technique: clamp(overall + (isGk ? -10 : isMid ? 5 : isForward ? 4 : -1)),
    physical: clamp(overall + (isDef ? 4 : primary === "ST" ? 3 : primary === "GK" ? 1 : 0)),
    mentality: clamp(overall + 1),
    goalkeeping: clamp(isGk ? overall + 8 : 38),
    creativity: clamp(overall + (["CAM", "CM", "LW", "RW", "CF"].includes(primary) ? 6 : isGk ? -22 : -4)),
    pace: clamp(overall + (["LW", "RW", "LB", "RB", "LWB", "RWB"].includes(primary) ? 5 : primary === "CB" ? -5 : 0)),
    finishing: clamp(overall + (["ST", "CF"].includes(primary) ? 8 : ["LW", "RW", "CAM"].includes(primary) ? 3 : -10)),
    passing: clamp(overall + (["CM", "CAM", "CDM"].includes(primary) ? 7 : isGk ? -12 : 0)),
    pressing: clamp(overall + (isMid ? 4 : isForward ? 1 : isDef ? 2 : -12)),
    discipline: clamp(overall + (primary === "CB" || primary === "CDM" ? -1 : 2)),
  };
};

const createPlayer = (squadId: string, player: SeedPlayer, index: number): Player => ({
  id: `${squadId}-${index + 1}`,
  name: player[0],
  positions: player[1],
  hiddenAttributes: positionProfile(player[1], player[2]),
  preferredFoot: index % 5 === 0 ? "Gauche" : index % 7 === 0 ? "Deux pieds" : "Droit",
  legendTag: player[3],
});

const p = (name: string, positions: Position[], overall: number, tag?: LegendTag): SeedPlayer => [
  name,
  positions,
  overall,
  tag,
];

